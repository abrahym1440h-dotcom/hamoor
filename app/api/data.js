// يختار المنافسين المناسبين حسب حجم الميزانية
export function getCompetitorsByBudget(sectorName, budget) {
  const list = COMPETITORS_BY_SECTOR[sectorName];
  if (!list || !list.length) return [];
  let tiers;
  if (budget >= 500000) tiers = ["راقي", "متوسط"];
  else if (budget >= 200000) tiers = ["متوسط", "راقي"];
  else tiers = ["اقتصادي", "متوسط"];
  // نرتّب: الفئة الأساسية أولاً ثم الثانوية
  const primary = list.filter(c => c.tier === tiers[0]);
  const secondary = list.filter(c => c.tier === tiers[1]);
  const rest = list.filter(c => !tiers.includes(c.tier));
  return [...primary, ...secondary, ...rest].slice(0, 6).map(c => c.name);
}

